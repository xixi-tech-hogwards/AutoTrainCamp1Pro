#!/usr/bin/env python
# -*- coding: utf-8 -*-

class Calc:

    def add(self, a, b):
        return a + b

    def add1(self, data):
        return data[0] + data[1]

    def div(self, a, b):
        return a / b

    def sub(self, a, b):
        return a - b

    def mul(self, a, b):
        return a * b
